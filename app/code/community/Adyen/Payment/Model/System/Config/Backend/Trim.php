class Adyen_Payment_Model_System_Config_Backend_Trim extends Mage_Core_Model_Config_Data
{

    /**
     * Trim data before save
     */
    protected function _beforeSave()
    {
        $value = trim($this->getValue());
        $this->setValue($value);
    }

}